#include <unistd.h>

int main(int ac, char *av[])
{
	int	i;

	if (ac == 2)
	{
		i = 0;
		while ((av[1][i] == ' ' || av[1][i] == '\t') && av[1][i] != '\0')
			i++;
		while(av[1][i] != '\0' && (av[1][i] >= 33 && av[1][i] <= 126))
		{
			write(1, &av[1][i], 1);
			if (av[1][i + 1] == 32)
				break;
			i++; 
		}
	}
	write(1, "\n", 1);
}