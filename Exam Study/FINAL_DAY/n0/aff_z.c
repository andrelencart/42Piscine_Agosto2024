#include <unistd.h>

void	aff_z(char *str)
{
	int	i = 0;
	while (str[i] != '\0')
		i++;
	write(1, "z\n", 2);
}
int	main(int ac, char *av[])
{
	if (ac != 2)
	{
		write(1, "z\n", 2);
	}
	else
		aff_z(av[1]);
}